package OrientaçãoObjetoLista4;

public class Calculadora {
	
	static public int soma(int a, int b) {
		return a + b;
	}
	
	static public int subtracao(int a, int b) {
		return a - b;
	}
	
	static public int multiplicacao(int a, int b) {
		return a * b;
	}
	
	static public float divisao(float a, float b) {
		return a / b;
	}
	
	static public int potencia(int a, int b) {
		return (int) Math.pow(a, b);
	}
}
